            <p>
                White Leaf - the German Rock / Metal band is releasing its second studio album »Metaera« in december
                2021 and reinventing themselves. Completely self-written, produced, recorded, mixed and mastered,
                their DIY sound runs through the entire new album. With the mixture of grooving, hard riffs, melodic
                guitars and vocals White Leaf remained true to themselves. At the same time, their experience and
                musical growth take their sound to a new level. The songs invite the listener on an exciting journey
                of self-reflection and always leave space for the listeners very own interpretation.
